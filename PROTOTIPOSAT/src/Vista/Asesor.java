    private void btnResgistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnResgistrarMouseClicked
        // TODO add your handling code here:
        
        String nombre=txtNombre.getText();
        String aParerno=txtApellPat.getText();
        String aMaterno=txtApellMat.getText();
        String cubiculo=txtCubiculo.getText();
        String email=txtEmail.getText();
        String horario=txtHorario1.getText()+""+lblHorario.getText()+""+txtHoraario2.getText();
        
        Asesores asesor =new Asesores(nombre, aParerno, aMaterno, cubiculo, email, horario);
        if(ahelp.guardarAsesor(asesor)){
            JOptionPane.showMessageDialog(null, "Asesor fue Guardada Exitosamente en la Base de Datos", "Hecho", JOptionPane.INFORMATION_MESSAGE);
            txtNombre.setText("");
            txtApellPat.setText("");
            txtApellMat.setText("");
            txtCubiculo.setText("");
            txtEmail.setText("");
            txtHorario1.setText("");
            txtHoraario2.setText("");
                    
        }
        else{
             JOptionPane.showMessageDialog(null, "Asesor no pudo ser Guardada en la Base de Datos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnResgistrarMouseClicked

