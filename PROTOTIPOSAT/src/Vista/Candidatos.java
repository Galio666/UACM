    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        if (txtBoleta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Boleta", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
         if (txtNomC.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Nombre", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
          if (txtAppC.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Apellido Paterno", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
          
          if (txtApMatC.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Apellido Materno", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
          
         if (txtCarrera.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Carrera", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }  
         
          if (txtCelular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Numero de Celular", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
          
           if (txtCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Correo Electronico", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
           
         if (txtDirtesis.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Director Tesis", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }  
         
         if (txtTema.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Tema de Tesis", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }  
         
         if ( txtLugarTrabajo.isEnabled()&& txtLugarTrabajo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Lugar de Trabajo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
         
         if ( txtHorario1.isEnabled()&& txtHorario1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Horario de Trabajo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
         
         if ( txtHorario2.isEnabled()&& txtHorario2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falto agregar Horario de Trabajo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } 
         
         String imagen = ruta;
         
         String nombre =txtNomC.getText();
         String aPat=txtApMatC.getText();
         String aMat=txtApMatC.getText();
         String boleta=txtBoleta.getText();
         String email=txtCorreo.getText();
         String celular=txtCelular.getText();
         String carrera=txtCarrera.getText();
         String generacion=txtGeneracio.getText();
         String dirTesis=txtDirtesis.getText();
         String temTesis=txtTema.getText();
         String trabaja=checkSI.getText()+checkNO.getText();
         
         String lugTrabajo=txtLugarTrabajo.getText();
         String horTrabajo=txtHorario1.getText()+lblhorario.getText()+txtHorario2.getText();
         
         
         Candidato candi =new Candidato(imagen, nombre, aPat, aMat, boleta, email, celular, carrera, generacion, temTesis, dirTesis, trabaja, lugTrabajo, horTrabajo);
         
         if (!ruta.equals("")) {
            
                if (canHelp.guardarImagen(candi)) {
                    JOptionPane.showMessageDialog(null, "Candidato fue Guardada Exitosamente en la Base de Datos", "Hecho", JOptionPane.INFORMATION_MESSAGE);
//                txtTitulo.setText("");
//                txtDirector.setText("");
//                txtActorP.setText("");
//                txtGenero.setText("");
//                txtPrecio.setText("");
//                lblimagen.setIcon(null);
                ruta = "";
//                v.visualizar_tabla(tabla);
                return;
                } else {
                    JOptionPane.showMessageDialog(null, "Candidato no pudo ser Guardada en la Base de Datos", "Error", JOptionPane.ERROR_MESSAGE);
                }
             
        } else {
            JOptionPane.showMessageDialog(null, "Te falta Agregar la foto", "Error", JOptionPane.ERROR_MESSAGE);
        }
         
         


    }//GEN-LAST:event_btnRegistrarActionPerformed

